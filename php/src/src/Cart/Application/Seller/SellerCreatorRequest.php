<?php

declare(strict_types = 1);

namespace App\Cart\Application\Seller;

class SellerCreatorRequest
{
    private $id;
    private $name;

    public function __construct(string $id, string $name)
    {
        $this->id = $id;
        $this->name = $name;
    }

    public function id(): string
    {
        return $this->id;
    }

    public function name(): string
    {
        return $this->name;
    }
}
